package ru.betuganova.Model;

/**
 * Represents different hair colors that a user can have.
 */
public enum HairColor {
    /** Black hair color. */
    BLACK,

    /** Brown hair color. */
    BROWN,

    /** Blond hair color. */
    BLOND,

    /** Red hair color. */
    RED,

    /** White or gray hair color. */
    WHITE,

    /** Unknown or unspecified hair color. */
    UNKNOWN
}
